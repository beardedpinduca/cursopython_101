"""
OPERADORES RELACIONAIS
== igualdade
> maior que
>= maior que ou igual a
< menor que
<= menor que ou igual a
!= é diferente de

Sempre retornará um valor booleano por padrão. (True ou False)
"""
print(2 == 1)  #Esta espressão está questionando, 2 é igual a 1?
print(2 > 1)  #Esta espressão está questionando, 2 é maior que 1?
print(2 >= 1)  #Esta espressão está questionando, 2 é maior ou igual a 1?
print(2 < 1)  #Esta espressão está questionando, 2 é menor que 1?
print(2 <= 1)  #Esta espressão está questionando, 2 é menor ou igual a 1?
print(2 != 1)  #Esta espressão está questionando, 2 é diferente de 1?

print('Jonas' == 'Jonas')  #Funciona também com strings e etc..